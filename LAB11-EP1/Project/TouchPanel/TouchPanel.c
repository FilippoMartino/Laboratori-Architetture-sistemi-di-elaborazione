/* Convert Color to LCD displayable */

int convertColor(uint16_t toConvert){
	uint8_t R, G, B;
		
	R = (toConvert & 0xf800) >> 8;
	G = (toConvert & 0x07e0) >> 3;
	B = (toConvert & 0x001f) << 3;
	
	return (uint16_t) ((( R >> 3 ) << 11 ) | (( G >> 2 ) << 5 ) | (B >> 3 ));

}

